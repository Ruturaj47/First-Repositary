package operators;

public class SwitchOperator {

	public static void main(String[] args) {
		int var=2;
		switch (var) {
		case 1:
			System.out.println("Jan");
			break;
		case 2:
			System.out.println("Feb");
			break;
		case 3:
			System.out.println("Mar");
			break;
		default:
			System.out.println("Please enter valid input");
		}
		
	}

}
