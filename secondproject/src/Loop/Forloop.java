package Loop;

public class Forloop {

	public static void main(String[] args) {
		
		//when you have to run some set of statements/code multiple time called as loop
		int i;
		for(i=1;i<=10;i++)
		//i have one variable whose name is i and its value is 1
		//Please run the code till i value is either less than 10 or equal to 10	
		{
			System.out.println(i);
		}
		
		for(i=100;i>=1;i=i-3) {
			System.out.println(i);
		}
	}
}
  